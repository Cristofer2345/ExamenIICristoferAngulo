    public void mostrartable(){
        for (int i = 0; i < listEmpleado.size(); i++) {
            Empleado empleado  =  listEmpleado.get(i); 
          if ( listEmpleado.get(i) != null) {
                 Object[] row = {empleado.getCedula(),empleado.getNombre(),empleado.getApellidos(),empleado.getGenero(),empleado.getFechaNac(),empleado.getEmail(),empleado.getSalario(),empleado.getProcComision()}; 
                 modelTable.addRow(row);
                lblEmpleado.setModel(modelTable);
          }
        }
    
    }
